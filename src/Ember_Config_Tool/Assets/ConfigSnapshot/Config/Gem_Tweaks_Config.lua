-- Master Toggle
Enable_GemTweaks = false

-- Salvage Rate
-- Ratio of return when salvaging gems.
-- Default = 0.5
GemTweaks_SalvageRate = 0.5

-- Equipment Gem Slot Probability
-- This controls the probability of a piece of equipment having a gemslot based on its rarity.
-- The default values mean that common equipment will never have a gemslot, while legendary equipment will always have a gemslot.

-- Common Equipment Gem Slot Probability
-- Default = 0.0
GemTweaks_Probability_Common = 0.0

-- Uncommon Equipment Gem Slot Probability
-- Default = 0.05
GemTweaks_Probability_Uncommon = 0.05

-- Rare Equipment Gem Slot Probability
-- Default = 0.25
GemTweaks_Probability_Rare = 0.25

-- Epic Equipment Gem Slot Probability
-- Default = 0.5
GemTweaks_Probability_Epic = 0.5

-- Legendary Equipment Gem Slot Probability
-- Default = 1.0
GemTweaks_Probability_Legendary = 1.0
